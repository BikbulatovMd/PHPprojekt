          <h2>Synopsis</h2>
          <p>
            The Lord of the Rings takes up the story about 60 years after the end of The Hobbit. The story begins in
            the first volume, The Fellowship of the Ring, when Frodo Baggins, Bilbo's adoptive heir, came into
            possession of Bilbo's magic ring. Bilbo's old friend, Gandalf the Grey, who got Bilbo involved in the
            adventures in The Hobbit that led to the discovery of the Ring, discovered that it was in fact the One
            Ring, the instrument of Sauron's power and the object for which the Dark Lord has been searching for most
            of the Third Age, and which corrupted others with desire for it and the power it held. Sauron sent the
            sinister Nazgûl, in the guise of riders in black, to the Shire, Frodo's native land, in search of the
            Ring. Frodo escaped, with the help of his loyal gardener Samwise Gamgee and three close friends, Meriadoc
            Brandybuck, Peregrin Took, and Fredegar Bolger. While Fredegar acted as decoy for the Ringwraiths, Frodo
            and the others set off to take the Ring to the Elven haven of Rivendell. They were aided by the enigmatic
            Tom Bombadil, who saved them from Old Man Willow and took them in for a few days of feasting, rest, and
            counsel. At the town of Bree, Frodo's party was joined by a man called "Strider", who was revealed, in a
            letter left by Gandalf at the local inn for Frodo, to be Aragorn, the heir to the thrones of Gondor and
            Arnor, two great realms founded by the Númenórean exiles. Aragorn led the hobbits to Rivendell on
            Gandalf's request. However, Frodo was gravely wounded by the leader of the Ringwraiths, though he managed
            to recover under the care of lord Elrond, the Half-elf. In Rivendell, the hobbits also learned that
            Sauron's forces could only be resisted if Aragorn took up his inheritance and fulfilled an ancient
            prophecy by wielding the sword Andúril, which had been forged anew from the shards of Narsil, the sword
            that cut the Ring from Sauron's finger in the Second Age. The treachery of Saruman, formerly head of the
            White Council and now in league with Sauron is also revealed. A high council, attended by representatives
            of the major races of Middle-earth; Elves, Dwarves, and Men, and presided over by Elrond, decide that the
            only course of action that can save Middle-earth is to destroy the Ring by taking it to Mordor and casting
            it into Mount Doom, where it was forged. Frodo volunteered for the task, and a "Fellowship of the Ring"
            was formed to aid him — consisting of Frodo, his three Hobbit companions, Gandalf, Aragorn, Boromir of
            Gondor, Gimli the Dwarf, and Legolas the Elf. Their journey took them through the Mines of Moria, where
            they began to be followed by the creature Gollum, whom Bilbo had met in the Goblin-caves of the Misty
            Mountains years before. (The full tale of their meeting is told in The Hobbit.) Gollum long possessed the
            Ring before it passed to Bilbo. Gandalf explained that Gollum belonged to a people "of hobbit-kind" before
            he came upon the Ring, which corrupted him. A slave to the Ring's evil power, Gollum desperately sought to
            regain his "Precious." As they proceeded through the Mines, Pippin unintentionally betrayed their presence
            and the party was attacked by creatures of Sauron. Gandalf battled an ancient demon, a Balrog, and fell
            into a deep chasm, apparently to his death. Escaping from Moria, the Fellowship, now led by Aragorn, go to
            the Elven realm of Lothlórien. Here, the Lady Galadriel showed Frodo and Sam visions of the past, present,
            and future. Frodo also saw the Eye of Sauron, a metaphysical expression of Sauron himself, and Galadriel
            was tempted by the Ring. By the end of the first volume, after the Fellowship has travelled along the
            great River Anduin, Frodo decided to continue the trek to Mordor on his own, largely due to the Ring's
            growing influence on Boromir; however, the faithful Sam insisted on going with him. In the second volume,
            The Two Towers, a parallel story, told in the first book of the volume, details the exploits of the
            remaining members of the Fellowship who aid the country of Rohan in its war against the emerging evil of
            Saruman, leader of the Order of Wizards, who wanted the Ring for himself. At the start of the first book,
            the Fellowship was further scattered; Merry and Pippin were captured by Sauron and Saruman's Orcs, Boromir
            was mortally wounded defending them, and Aragorn and the others went off in pursuit of the Hobbits
            captors. The three meet Gandalf, who has returned to Middle-earth as "Gandalf the White": they found out
            that he slew the Balrog of Moria, and although the battle also proved fatal to Gandalf, he was then sent
            back and "reborn" as a more imposing figure. At the end of the first book, Gandalf, Aragorn, Legolas and
            Gimli help defeat Saruman's armies at the Battle of the Hornburg while Saruman himself was cornered by the
            tree-like Ents and Huorns, accompanied by Merry and Pippin, who escaped from captivity, and the two groups
            are reunited. The second book of the volume tells of Frodo and Sam's exploits on the way to Mount Doom.
            They managed to capture and "tame" Gollum, who then showed them a way to enter Mordor secretly (as opposed
            to the Black Gate), albeit through the dreaded realm of Minas Morgul. At the end of the volume, Gollum
            betrayed Frodo to the great spider, Shelob, and though he survived, he was captured by Orcs. Meanwhile,
            Sauron launched an all-out military assault upon Middle-earth, with the Witch-king (leader of the
            Ringwraiths) leading a fell host from Minas Morgul into battle against Gondor, in the War of the Ring. In
            the third volume, The Return of the King, the further adventures of Gandalf, Aragorn and company are
            related in the first book of the volume, while Frodo and Sam's are related in the second, as with The Two
            Towers. As told in the first book, the Fellowship assisted in the final battles against the armies of
            Sauron, including the siege of the city of Minas Tirith in Gondor and the climactic life-or-death battle
            before the Black Gate of Mordor, where the alliance of Gondor and Rohan fought desperately against
            Sauron's armies in order to distract him from the Ring, hoping to gain time for Frodo to destroy it. In
            the second book, Sam rescued Frodo from captivity. After much struggle, they finally reached Mount Doom
            itself, tailed by Gollum. However, the temptation of the Ring proved too great for Frodo and he claimed it
            for himself. However, Gollum struggled with him and managed to bite the Ring off. Crazed with triumph,
            Gollum slipped into the fires of the mountain, and the Ring was destroyed. Finally, Sauron was defeated,
            and Aragorn was crowned king. However, all was not over, for Saruman managed to escape and scour the Shire
            before being overthrown. At the end, Frodo remained wounded in body and spirit and went west accompanied
            by Bilbo over the Sea to Valinor, where he could find peace. According to Tolkien's timeline, the events
            depicted in the story occurred between Bilbo's announcement of his September 22, 3001 birthday party, and
            Sam's return to Bag End on October 6, 3021. Most of the events portrayed in the story occur in TA 3018 and
            TA 3019, with Frodo heading out from Bag End on September 23 3018, and the destruction of the Ring six
            months later on March 25, 3019.
          </p>
