          <h2>Backstory</h2>
          <p>
            The back story begins thousands of years before the action in the book, with the rise of the eponymous
            Lord of the Rings, the Dark Lord Sauron, a malevolent reincarnated deity who possessed great supernatural
            powers and who later became the ruler of the dreaded realm of Mordor. At the end of the First Age of
            Middle-earth, Sauron survived the catastrophic defeat and chaining of his lord, the ultimate Dark Lord,
            Morgoth (who was formerly counted as one of the Valar, the angelic Powers of the world). During the Second
            Age, Sauron schemed to gain dominion over Middle-earth. In the disguise as "Annatar" or Lord of Gifts, he
            aided Celebrimbor and other Elven-smiths of Eregion in the forging of magical Rings of Power which
            conferred various powers and effects on their wearers. The most important of these were the Nine, the
            Seven and the Three (which he did not touch or know of the three.) called the Rings of Power or Great
            Rings. However, he then secretly forged a Great Ring of his own, the One Ring, by which he planned to
            enslave the wearers of the other Rings of Power. This plan failed when the Elves became aware of him and
            took off their Rings. Sauron then launched a war during which he captured sixteen and distributed them to
            lords and kings of Dwarves and Men; these Rings were known as the Seven and Nine respectively. The
            Dwarf-lords proved too tough to be enslaved, although their natural desire for wealth, especially gold,
            increased; this brought more conflict between them and other races, and fed a dangerous greed. The Men who
            received the Nine rings were slowly corrupted over time and eventually became the Nazgûl, Sauron's most
            feared servants. The Three Rings Sauron failed to capture, and remained in the possession of the Elves
            (who forged these independently). The war ended as Men of the island-kingdom of Númenor helped the
            besieged Elves, and Sauron's forces were practically destroyed. At this time he still held most of
            Middle-earth, excluding the coasts, Imladris (Rivendell) and the Gulf of Lune.
          </p>
          <p>
            Over 1500 years later, word reached the King of Númenor, Ar-Pharazôn, that Sauron had claimed the title
            "King of Men". This provoked Ar-Pharazôn and gave him an opportunity to display the glory and strength of
            Númenor. He arrived in Middle-earth with such overwhelming force that Sauron's armies fled at the sight of
            the Númenóreans. Abandoned by his minions, Sauron surrendered to the Númenóreans, and was taken to Númenor
            as a "prisoner". Sauron then started to poison the minds of the Númenóreans against the Valar. Thus,
            Sauron set into motion events that brought about Númenor's destruction. He did this by corrupting the
            King's mind, telling him that the immortality of the Elves was his to take if he set foot upon the lands
            of Aman, the Blessed Realm, where Valinor, the realm of the Valar, was located. Fearing death, Ar-Pharazôn
            led an invasion of Aman and Valinor with the greatest host seen since the end of the First Age. However,
            upon reaching Aman, he and his army were buried by a landslide, and there they would remain until the
            Final Battle in Tolkien's eschatology. Manwë, the King of Arda, called upon Eru Ilúvatar (God), who opened
            a great chasm in the sea, destroying Númenor, and removed the Undying Lands from the mortal world. The
            destruction of Númenor destroyed Sauron's physical body and his ability to take a fair and handsome shape,
            but his spirit returned to Mordor and assumed a new form: black, "burning hot", and terrible. Over 100
            years later, he launched an attack against the Númenórean exiles (The Faithful, who did not join
            Ar-Pharazôn's expedition), who managed to escape to Middle-earth. However, the exiles (led by Elendil and
            his sons Isildur and Anárion) had time to prepare, and, after forming the Last Alliance of Elves and Men
            with the Elven-king Gil-galad, they marched against Mordor, defeated Sauron on the plain of Dagorlad, and
            besieged Barad-dûr, at which time Anárion was slain. After seven years of siege, Sauron himself was
            ultimately forced to engage in single combat with the leaders. Gil-galad and Elendil perished in the
            struggle, and Elendil's sword, Narsil, broke beneath him. However, Sauron's body was also overcome and
            slain, and Isildur cut the One Ring from Sauron's hand with the hilt-shard of Narsil, and at this Sauron's
            spirit fled and he did not reappear for a millenium. Isildur was advised to destroy the One Ring by the
            only way it could be — by casting it into the volcanic Mount Doom where it was forged — but he refused,
            attracted to its beauty and kept it as compensation for the deaths of his father and brother (weregild).
            So began the Third Age of the Sun. Two years later, while journeying to Rivendell, Isildur and his
            soldiers were ambushed by a band of Orcs at the Disaster of the Gladden Fields. While the latter were
            almost all killed, Isildur escaped by putting on the Ring — which made mortal wearers invisible. However,
            the Ring slipped from his finger while he was swimming in the great River Anduin; he was killed by
            Orc-arrows and the Ring was lost for two millennia. It was then found by chance by a Stoor named Déagol.
            His relative and friend Sméagol strangled him for the Ring and was banished from his home by his maternal
            grandmother. He fled to the Misty Mountains where he slowly withered and became a disgusting, slimy
            creature called Gollum. In The Hobbit, set 60 years before the events in The Lord of the Rings, Tolkien
            relates the story of the seemingly accidental finding of the Ring by another hobbit, Bilbo Baggins, who
            takes it to his home, Bag End. Story-externally, the tale related in The Hobbit was written before The
            Lord of the Rings, and it was only later that the author developed Bilbo's magic ring into the "One Ring."
            Neither Bilbo nor the wizard, Gandalf, are aware at this point that Bilbo's magic ring is the One Ring,
            forged by the Dark Lord Sauron.
          </p>
