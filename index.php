        <section class="story" id="frodo">
          <h2>Frodo</h2>
          <p>
            Frodo Baggins, son of Drogo Baggins, was a hobbit of the Shire in the late Third Age. He is commonly
            considered Tolkien's most renowned character for his leading role in the Quest of the Ring, in which he
            bore the One Ring to Mount Doom, where it was destroyed. He was a Ring-bearer, best friend to his
            gardener, Samwise Gamgee, and was one of three hobbits who sailed from Middle-earth to the Uttermost West
            at the end of the Third Age.
          </p>
          <h4>Chilhood</h4>
          <p>
            Much of Frodo's youth was spent at Brandy Hall in Buckland, the ancestral home of the Brandybuck family,
            including his mother (Primula Brandybuck). Frodo was known as something of a rascal, befriending Meriadoc
            (Merry) Brandybuck and Peregrin (Pippin) Took and causing trouble wherever they went. They would often
            steal mushrooms from Farmer Maggot's farm Bamfurlong. In TA 2980, when Frodo was only 12 years old, his
            parents drowned in a boating accident on the Brandywine River. An only child, Frodo stayed in Brandy Hall
            until his 99-year-old "uncle" Bilbo adopted him in TA 2989. Bilbo took Frodo to live with him in his home
            at Bag End and made him his heir.The two grew very close in the following years; Frodo learned much of the
            Elvish language during his time with Bilbo, as well as much of the lore of Middle-earth. The two shared
            the same birthday, September 22 by Shire Reckoning (around September 12–14 of our calendar), and a party
            of special magnificence was held when Frodo came of age of thirty-three and Bilbo achieved the notable age
            of 111. Bilbo gave a memorable birthday speech before playing a joke on his fellow hobbits by using the
            One Ring to disappear, at which Gandalf quickly reacted and used his staff to create a blinding flash
            where Bilbo had been standing. The hobbits at the party were left confused and disgruntled, and Bilbo was
            never again seen in the Shire. Before departing for his journey to Rivendell, Bilbo had a long
            conversation with Gandalf, who finally persuaded him to voluntarily surrender the One Ring. Bilbo left it
            on the fireplace mantel with a note for Frodo, who would now become the next Ring-bearer.
          </p>
        </section>
        <section class="story" id="bilbo">
          <h2>Bilbo</h2>
          <p>
            Bilbo Baggins was a Hobbit of the Shire, the main protagonist of The Hobbit and a secondary character in
            The Lord of the Rings. Gandalf suggested to Thorin and Company that they hire Bilbo Baggins to be their
            burglar in the Quest of Erebor, and later fought in the Battle of Five Armies. Bilbo was also one of the
            bearers of the One Ring, and the first to voluntarily give it up, although with some difficulty. He wrote
            many of his adventures in a book he called There and Back Again. Bilbo adopted his
            second-cousin-once-removed Frodo Baggins to be his heir after his parents, Drogo Baggins and Primula
            Brandybuck, drowned in the Brandywine River.Bilbo was the first hobbit to become famous in the world at
            large, and one of the few to set foot in the Undying Lands.Bilbo Baggins was born on September 22 by Shire
            Reckoning (around September 12-14 of our calendar), in the year 2890 of the Third Age. He was the only son
            of Bungo Baggins and Belladonna Took. In Hobbiton, Bungo Baggins constructed a spacious and luxurious
            Hobbit-hole for Belladonna, which they named Bag End. The family moved to their new home, where Bilbo
            would spend much of his life.As a young Hobbit, Bilbo was curious and eager for news of the outside world.
            The Istar wizard, Gandalf, took interest in this unusual quality in Bilbo during his visits to the Shire.
            Bilbo would later remember Gandalf's Firework displays in the dwelling of his mother's family at Great
            Smials. Bilbo apparently practiced his rock-throwing skills in his youth so much, that birds and squirrels
            fled the area whenever he bent down to pick up a rock. When Bilbo's father and mother died in TA 2926 and
            TA 2934 respectively, Bilbo became his own master and spent the next seven years living alone in Bag End.
            During this time, Bilbo grew fond of his life as a wealthy bachelor and acquired a reputation for
            respectability that the neighbors admired.In the year TA 2941, Bilbo, at the age of 50, was visited by
            Gandalf, whom he had not seen for quite some time, outside his home at Bag End. Gandalf invited Bilbo on
            an unspecified adventure, which was flatly refused by the hobbit, who bade the wizard a "Good morning",
            and went inside. Undeterred, Gandalf continued in his plan, sending thirteen Dwarves, led by Thorin
            Oakenshield, to Bag End the very day. These Dwarves, needing an additional member for their company,
            initially came by pairs, and lastly with Gandalf, who had himself selected Bilbo as the best person for
            the Dwarves' job. The Dwarves explained to Bilbo their background and purpose; that they were among the
            Dwarves of Erebor who had been forced from the Lonely Mountain when the dragon Smaug attacked and taken up
            residence there. They now had determined to reclaim the mountain and needed a "burglar" to help them in
            situations requiring stealth. Bilbo reluctantly agreed to go, and the next morning they set off from the
            Green Dragon Inn.
          </p>
