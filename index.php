          <h2>The Lord of the Rings</h2>
          <p>
            The Lord of the Rings is an epic high fantasy novel written by J.R.R. Tolkien, which was later fitted as a
            trilogy. The story began as a sequel to Tolkien's earlier fantasy book The Hobbit, and soon developed into
            a much larger story. At publication, it was divided into The Fellowship of the Ring, The Two Towers, and
            The Return of the King. Written in stages between 1937 and 1949, with much of it during World War II, it
            was originally published in three volumes in 1954 and 1955. It has since been reprinted numerous times and
            translated into at least 38 different languages, becoming one of the most popular works of
            twentieth-century literature. The action in The Lord of the Rings is set in what the author conceived to
            be the lands of the real Earth, inhabited by humanity but placed in a fictional past, before familiar
            history but after the fall of his version of Atlantis, Númenor. Tolkien gave this setting a modern English
            name, Middle-earth, a rendering of the Old English Middangeard.
          </p>
