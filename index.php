            <h2>Main conflict</h2>
            <p>
              Tolkien offered The Lord of the Rings to Allen Publishing & Unwin. According to Tolkien's plan, the
              Silmarillion should have been published simultaneously with The Lord of the Rings, but the publishing
              house did not go for it. Then in 1950 Tolkien offered his work to the Collins publishing house, but an
              employee of the publishing house Milton Waldman said that the novel "urgently needs to be cut." In 1952
              Tolkien wrote again in Allen & Unwin. In particular, he wrote, "I will gladly consider publishing any part
              of the text." The publishing house agreed to publish the novel in its entirety and without "cuts".
              According to the contract, the author did not receive anything, but after the costs of publishing the book
              were paid off, the author received a significant share of sales. Due to the lack of paper in the post-war
              period, the publishers asked Tolkien to split the novel into 6 books with no titles. Then, for
              convenience, they were combined in pairs by the publisher into three parts, which were called the
              "Brotherhood of the Ring" (or "Commonwealth of the Ring" or "Guardians", Eng. The Fellowship of the Ring;
              books 1 and 2), "Two Fortresses" (or "Two Towers" or "Two Strongholds", Eng. The Two Towers; books 3 and
              4), and "The Return of the King" (or "The Return of the Sovereign" Eng. The Return of the King; books 5
              and 6, appendices). Tolkien himself was against the division of his novel into these parts and agreed to
              it only when no publisher agreed to print the whole book. The point here was both the lack of paper, and
              the reduction in the price of the first volume, and the evaluation of the success of the series. Due to
              delays in the creation of a map of Middle-earth and applications, the novel was printed only in 1954-1955.
              Three parts were published in England on July 29, 1954, November 11, 1954 and October 20, 1955, and
              somewhat later in the USA. The delay in the publication of the last part was caused, in particular, by
              disputes over its title. Tolkien did not like "The Return of the King" and he suggested calling this part
              "The War of the Ring", but the publishers resisted and insisted on their own version. Since then, the Lord
              of the Rings has been called a trilogy. Tolkien himself used this term, although in other cases he said
              that it was wrong. The novel was published by various publishers in one, two, three, six or seven volumes.
              The book was received by the public in different ways. In general, most of the reviews were positive, and
              Tolkien's colleagues at the Inklings club were simply delighted. John's friend, Clive S. Lewis, predicted
              that the book would outlive its time. At the same time, many newspaper critics noted conservatism and
              insufficient depth of characters. Critic Edmund Wilson noted that Tolkien's book would be better suited to
              a younger audience, rather than adults.
            </p>
