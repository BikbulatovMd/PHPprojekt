          <h2>Gandalf</h2>
          <p>
            Gandalf the Grey, later known as Gandalf the White, and originally named Olórin (Quenya; IPA: [oˈloːrin]),
            was an Istar (Wizard), dispatched to Middle-earth in the Third Age to combat the threat of Sauron. He
            joined Thorin II Oakenshield and his company to reclaim the Lonely Mountain from Smaug, helped form the
            Fellowship of the Ring to destroy the One Ring, and led the Free Peoples in the final campaign of the War
            of the Ring.riginally called Olórin, he was accounted as the wisest of the Maiar. He was created by
            Ilúvatar before the Music of the Ainur, and was at the beginning of Time amongst the Ainur who entered
            into Eä. In his "youth" he became one of the Maiar who served Manwë, Varda, Irmo, and Nienna. He was
            associated with light and fire, much like Varda, yet his ways often took him to Nienna, from whom he
            learned pity and patience. He loved the Elves, but walked unseen among them, or wore the guise of an Elf,
            and sent them fair visions in their hearts that made them wiser. Throughout his life he followed the
            example of Nienna, talking pity on the sorrows of the Children of Ilúvatar and drawing those who listened
            to him out of despair.When the Valar decided to send the Istari (also called Wizards) to Middle-earth to
            counsel and assist all those who opposed the Dark Lord Sauron, Manwë and Varda included Olórin among the
            five to be sent. At first, Olórin was nervous and described himself as too weak and afraid of Sauron.
            Manwë understood, and told Olórin that such was a reason why he should go, to overcome that fear.
            Furthermore, the One Ring, containing much of Sauron's power, presumably still existed somewhere. Thus,
            Manwë insisted that Olórin should go as the third, but Varda convinced him to send Olórin as the second
            instead. He agreed, and prepared for Olórin's departure from the Undying Lands with the other four,
            arriving about the same time that the Necromancer appeared in Mirkwood.
          </p>
        </section>
        <section class="story" id="sauron">
          <h2>Sauron</h2>
          <p>
            Sauron (Quenya; IPA [ˈsaʊron] or [ˈθaʊron]), the eponymous Lord of the Rings, was the greatest subversive
            Maia, the maker of the One Ring, renowned student of the Vala Aulë, and the most trusted lieutenant of
            Morgoth. After Morgoth's ousting by the Valar, Sauron became the second Dark Lord and sought to conquer
            Middle-earth and even Númenor through guileful trickery - the chief representation of which were the Rings
            of Power - or overwhelming legions. At the end of the Second Age, Sauron was defeated in the War of the
            Last Alliance of Elves and Men united under Gil-galad and Elendil. In the final battle, Isildur managed to
            cut the One Ring from him, dismantling Sauron's corporeal form and ridding him of much of his power. After
            centuries lying dormant, rebuilding strength and stirring awake evil things, Sauron returned to power late
            in the Third Age, but was permanently crippled in the War of the Ring after the destruction of his Ring in
            the fires of Mount Doom by Frodo Baggins and his spirit was claimed by the Void.Sauron's original name was
